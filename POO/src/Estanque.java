public class Estanque {

    // attributes
    private int capacidad;

    // constructor
    public Estanque() {}

    public Estanque(int capacidad){
        this.capacidad = capacidad;

    }

    // Getters & setters
    public void setCapacidad(int capacidad){
        this.capacidad = capacidad;
    }
    public int getCapacidad(){
        return capacidad;
    }

}
