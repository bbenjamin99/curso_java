package org.bbenjamin.appfacturas.modelo;

public class Cliente {
    // attributes
    private String nombre;
    private String nif;

    // Getter & Setter

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNif() {
        return nif;
    }

    public void setNif(String nif) {
        this.nif = nif;
    }

    
}
