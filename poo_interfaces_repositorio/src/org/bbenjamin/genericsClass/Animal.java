package org.bbenjamin.genericsClass;

public class Animal {
    //Attributes
    private String nombre;
    private String tipo;
    //Constructor
    public Animal(String nombre, String tipo) {
        this.nombre = nombre;
        this.tipo = tipo;
    }
    // Getters 
    public String getNombre() {
        return nombre;
    }
    public String getTipo() {
        return tipo;
    }
    

    
}
